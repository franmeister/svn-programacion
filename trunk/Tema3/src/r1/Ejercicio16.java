package r1;

public class Ejercicio16 {

	public static void main(String[] args) {
		int a=2,b=-2,res=a+b;
		
		if (res<0){
			System.out.print("La suma de los números es negativa.");
		}else if (res>0){
			System.out.print("La suma de los números es positiva.");
		}else{
			System.out.print("La suma de los números es cero.");
		}
	}
}
/* Programa que reciba dos números 'a' y 'b' e indique si su suma es
positiva, negativa o cero. */