package r1;

public class Ejercicio14 {

	public static void main(String[] args) {
		int a=10,b=10;
		if (b==Math.sqrt(a)){
			System.out.print(a+" es el cuadrado de "+b);
		}else{
			System.out.print(a+" no es el cuadrado de "+b);
		}
	}
}

/* Realizar un programa que reciba dos valores enteros y devuelva la frase
“a es el cuadrado de b” si el primer valor es el cuadrado del segundo, y
“a no es el cuadrado de b” en caso contrario. */