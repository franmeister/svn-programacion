package r2;

public class Ejercicio15w {

	public static void main(String[] args) {
		int i=0, n=Integer.parseInt(args[0]);

		while (i<=10){
			System.out.println(n+" x "+i+" = "+(i*n));
			i++;
		}
	}

}
